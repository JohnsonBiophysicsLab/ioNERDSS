# visualize the model
def sig_vis(list, ax):
    for i in range(1, 6):
        figure = ax.plot([list[0][0], list[i][0]], [list[0][1], list[i][1]], [list[0][2], list[i][2]])

def vis(list):
    fig = plt.figure(1)  
    ax = fig.gca(projection='3d')
    for element in list:
        sig_vis(element, ax) 
    plt.tight_layout()    
    ax.set_xlabel('x (nm)')
    ax.set_ylabel('y (nm)')
    ax.set_zlabel('z (nm)')
#     plt.savefig('1.png', dpi=500)
    plt.show()

